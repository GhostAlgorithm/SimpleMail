    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        if (txtEmail.getText().isEmpty() && txtPassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Los campos Correo y Password son obligatorios!");
            return;
        }
        
        try {
            PrintWriter arch = new PrintWriter(new BufferedWriter(new FileWriter("cuentas_correo.txt", true)));
            
            byte[] md5Hash = new byte[5];
            MessageDigest md = MessageDigest.getInstance("MD5");
            md5Hash = md.digest(txtPassword.getText().getBytes());
            String md5 = new BigInteger(1, md5Hash).toString(16);
            
            arch.println(txtEmail.getText());
            arch.println(md5);
            arch.close();
            
            this.padre.tablaUsuarios.insertarUsuario(new Usuario(txtEmail.getText(), md5));
        } catch(Exception e){
            System.out.println("Error: " + e.toString());
        }
        JOptionPane.showMessageDialog(this, "Cuenta de correo creada con Ã©xito");
        this.setVisible(false);
    }//GEN-LAST:event_btnGuardarActionPerformed

